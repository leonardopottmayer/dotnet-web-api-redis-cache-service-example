using WebApi.Redis.Cache.Configuration;

namespace WebApi.Redis.Example
{
    public class Program
    {
        public static void Main(string[] args)
        {
            var builder = WebApplication.CreateBuilder(args);

            builder.Services.AddControllers();
            builder.Services.AddEndpointsApiExplorer();
            builder.Services.AddSwaggerGen();

            string cacheServiceConnectionString = builder.Configuration.GetSection("CacheService")["ConnectionString"];
            builder.Services.ConfigureCacheService(cacheServiceConnectionString);

            var app = builder.Build();

            if (app.Environment.IsDevelopment())
            {
                app.UseSwagger();
                app.UseSwaggerUI();
            }

            app.UseHttpsRedirection();
            app.UseAuthorization();
            app.MapControllers();

            app.Run();
        }
    }
}
